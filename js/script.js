'use strict';

const a = prompt('Enter first number:');
const b = prompt('Enter second number:');


const num1= Number(a);
const num2= Number(b);


const addition = num1 + num2;
console.log(`Answer: ${num1} + ${num2} = ${addition}`);


const subtraction = num1 - num2;
console.log(`Answer: ${num1} - ${num2} = ${subtraction}`);

const multiplication= num1 * num2;
console.log(`Answer: ${num1} * ${num2} = ${multiplication}`);

const division= num1 / num2;
console.log(`Answer: ${num1} / ${num2} = ${division}`);



